<h1>Welcome</h1>
<p>
    ACES (Atomic Clock Ensemble in Space) is a space mission whose heart is an ensemble of atomic clocks on board the international space station (ISS) and microwave and optical links to compare the onboard clocks to clocks on the ground. It features a cold atom clock (PHARAO) that will bring unprecedented accuracy into space, together with world-wide dissemination of its time-scale to ground clocks. The launch of ACES/PHRAO is expected for mid 2018.
</p>
<p>
    The science objectives are in fundamental physics, time/frequency metrology, cold-atom space physics, study of the ionosphere and geodesy. One of its primary goals is a measurement of the gravitational redshift, a central prediction of Einsteinâ€™s general relativity and a fundamental constituent of the Einstein Equivalence Principle (EEP), the experimental foundation of all metric theories of gravitation.
</p>
<p>
    The colloquium will bring together the scientific community interested in the results of ACES/PHARAO in all domains, from theoretical physics to cold atoms and application in geodesy and atmospheric studies. It will consist of invited and contributed presentations on the details of ACES, data analysis and scientific applications. Whilst centered on ACES/PHARAO the colloquium is open to more general contributions on theory and experiments in any of the related subject areas. The aim is to prepare within the broad scientific community for the upcoming launch (2018) and scientific exploitation of the data.
</p>

